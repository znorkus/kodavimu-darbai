function changeColorToYellow() {
  const p = document.querySelector("p");
  p.style.color = "yellow";
}

function changeColorToGreen() {
  const p = document.querySelector("p");
  p.style.color = "green";
}

function changeColorToRed() {
  const p = document.querySelector("p");
  p.style.color = "red";
}
